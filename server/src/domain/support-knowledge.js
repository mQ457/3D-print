const SITE_MAP = `
- landing.html: главная страница с описанием сервиса и переходом к оформлению.
- print-step-1.html -> print-step-2.html -> print-step-3.html: пошаговый сценарий заказа 3D-печати.
- checkout.html: подтверждение параметров заказа перед оплатой.
- delivery-address.html: добавление и выбор адреса доставки.
- payment.html: добавление/выбор карты и оплата.
- orders.html: список заказов пользователя и их статусы.
- profile.html: профиль, обращения в поддержку, история диалогов.
- login.html: вход и регистрация.
`.trim();

const SITE_GUIDE = `
Навигация по сайту:
1) Пользователь обычно начинает с главной страницы и шага печати.
2) Для оформления заказа нужно пройти шаги печати и перейти в checkout.
3) После checkout пользователь задает адрес доставки и способ оплаты.
4) В profile доступны редактирование профиля и чат с поддержкой.
5) В orders пользователь отслеживает статус заказа.
`.trim();

const PRINT_BASICS = `
Базовые знания по 3D-печати:
- FDM: универсально и дешевле, подходит для прототипов и функциональных деталей.
- SLA: высокая детализация и гладкая поверхность, подходит для миниатюр/мелких точных элементов.
- SLS: прочные нейлоновые детали сложной формы без поддержек, обычно дороже.

Материалы:
- PLA: простой, недорогой, для макетов и несложных задач.
- ABS: прочнее и термостойче PLA, но сложнее в печати.
- PETG: компромисс прочности и устойчивости к влаге, хороший универсальный вариант.
- Resin (смола): для очень высокой детализации.

Толщина слоя:
- 0.1 мм: выше качество, дольше печать.
- 0.2 мм: баланс скорость/качество.
- 0.3 мм: быстрее, но грубее поверхность.
`.trim();

const ADVICE_RULES = `
Правила рекомендаций:
- При недостатке данных задавай уточняющие вопросы: назначение детали, размер, важна ли прочность/точность, бюджет, сроки.
- Для "что выбрать" давай 2-3 варианта с плюсами/минусами.
- Не обещай физически невозможные сроки и характеристики.
- Если вопрос требует действий оператора (возврат, спорный платеж, ручная правка заказа), предлагай подключить консультанта.
`.trim();

function buildKnowledgeContext(userMessage) {
  const text = String(userMessage || "").toLowerCase();
  const wantsSiteHelp =
    /(сайт|страниц|куда нажать|как оформить|как заказать|как оплат|как войти|где|профил|заказ)/i.test(text);
  const wantsPrintHelp =
    /(3d|3-д|печат|sla|sls|fdm|pla|abs|petg|смол|материал|слой|толщин|прочност|детализац)/i.test(text);

  const blocks = [];
  if (wantsSiteHelp) {
    blocks.push(`Структура сайта:\n${SITE_MAP}`);
    blocks.push(`Подсказки по навигации:\n${SITE_GUIDE}`);
  }
  if (wantsPrintHelp) {
    blocks.push(`Знания по 3D-печати:\n${PRINT_BASICS}`);
  }
  if (blocks.length === 0) {
    blocks.push(`Ключевая структура сайта:\n${SITE_MAP}`);
  }
  blocks.push(`Правила консультации:\n${ADVICE_RULES}`);
  return blocks.join("\n\n");
}

